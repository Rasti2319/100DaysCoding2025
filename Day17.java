public class ModulusExample {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        // Menggunakan modulus//
        int hasil = a % b;

        System.out.println("Bilangan a = " + a);
        System.out.println("Bilangan b = " + b);
        System.out.println("Sisa hasil bagi a % b = " + hasil);
    }
}
